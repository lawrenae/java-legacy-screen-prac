package com.neopragma.legacy.screen;

public class CityState {
    private String city;
    private String state;


    public CityState(String city, String state) {
        this.city = city;
        this.state = state;
    }

    public String getCity() {
        return city;
    }

    public String getState() {
        return state;
    }
}
